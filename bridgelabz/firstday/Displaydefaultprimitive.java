package com.bridgelabz.day1;

public class Displaydefaultprimitive {
		
	static int a;
	static byte b;
	static short c;
	static long d;
	static float e;
	static double f;
	static boolean g;
	static char h;
	
	public static void main(String[] args) {
		
		
		System.out.println(a +"int");
		System.out.println(b +"byte");
		System.out.println(c +"short");
		System.out.println(d +"long");
		System.out.println(e + "float");
		System.out.println(f + "double");
		System.out.println(g + "boolean");
		System.out.println(h + "char");

	}

}
